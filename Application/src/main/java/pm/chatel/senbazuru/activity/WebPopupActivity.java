package pm.chatel.senbazuru.activity;

import android.content.Intent;
import android.os.Bundle;
import android.support.v7.widget.Toolbar;
import android.view.MenuItem;
import android.webkit.WebView;

import pm.chatel.senbazuru.R;

public class WebPopupActivity extends BaseActivity {

    public static final String ACTION_LOAD_ABOUT = "http://senbazuru.fr/android/about_web_reflow_export/page.html";

    private WebView mWebView;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_web);

        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);
        getSupportActionBar().setDisplayHomeAsUpEnabled(true);

        mWebView = (WebView) findViewById(R.id.webView);
        mWebView.getSettings().setJavaScriptEnabled(true);

        Intent intent = getIntent();
        if (intent != null) {
            mWebView.loadUrl(intent.getAction());
        } else {
            mWebView.loadUrl(ACTION_LOAD_ABOUT);
        }
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem menuItem) {
        switch (menuItem.getItemId()) {
            case android.R.id.home:
                finish();
                return true;
        }
        return (super.onOptionsItemSelected(menuItem));
    }

}

